// Loops

// While loop
// for loop
// do-while loop

#include <iostream>
using namespace std;

int main()
{
    // While Loop
    int count = 1;
    while (count <= 5)
    {
        cout << count << endl;
        count++;
    }

    // for loop
    int n = 2;
    for (int i = 0; i < n; i++)
    {
        cout << i << endl;
    }

    return 0;
}
